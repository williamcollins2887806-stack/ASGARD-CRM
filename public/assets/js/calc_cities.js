// Функция поиска города
window.findCity = function(query) {
  if (!query || query.length < 2) return [];
  const q = query.toLowerCase().trim();
  return CALC_CITIES.filter(c => 
    c.name.toLowerCase().includes(q) || 
    c.region.toLowerCase().includes(q)
  ).slice(0, 20);
};

// Функция получения расстояния
window.getCityDistance = function(cityName) {
  const city = CALC_CITIES.find(c => c.name.toLowerCase() === cityName.toLowerCase().trim());
  return city ? city.km : null;
};

