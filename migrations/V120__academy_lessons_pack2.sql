-- Защита от повторного выполнения (если week уже занят — DO NOTHING)
-- В insert_lesson() возвращаем id (или NULL если конфликт по week_number)

CREATE OR REPLACE FUNCTION _academy_insert_lesson(
  p_week int, p_saga text, p_title text, p_icon text, p_color text,
  p_minutes int, p_tags text[], p_mandatory boolean, p_blocks jsonb
) RETURNS int AS $$
DECLARE v_id int;
BEGIN
  INSERT INTO academy_lessons
    (week_number, saga, title, cover_icon, cover_color, estimated_minutes,
     tags, status, generated_by, blocks, is_mandatory, published_at)
  VALUES
    (p_week, p_saga, p_title, p_icon, p_color, p_minutes,
     p_tags, 'published', 'manual', p_blocks, p_mandatory, NOW())
  ON CONFLICT (week_number) DO NOTHING
  RETURNING id INTO v_id;
  RETURN v_id;
END;
$$ LANGUAGE plpgsql;

CREATE OR REPLACE FUNCTION _academy_insert_q(
  p_lesson_id int, p_sort int, p_type text, p_text text,
  p_options jsonb, p_explanation text
) RETURNS void AS $$
BEGIN
  IF p_lesson_id IS NOT NULL THEN
    INSERT INTO academy_quiz_questions
      (lesson_id, sort_order, question_type, question_text, options, correct_explanation)
    VALUES
      (p_lesson_id, p_sort, p_type, p_text, p_options, p_explanation);
  END IF;
END;
$$ LANGUAGE plpgsql;

