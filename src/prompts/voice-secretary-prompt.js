const VOICE_OPERATOR_USER = (context) => {
  const parts = [];

  // Внутренний звонок — сотрудник
  if (context.isInternal) {
    parts.push(`ЗВОНЯЩИЙ: Сотрудник ${context.internalCaller.name} (${context.internalCaller.role})`);
    parts.push(`Номер: ${context.callerNumber}`);
  } else
  // Информация о звонящем
  if (context.clientName || context.clientCompany) {
    parts.push(`ЗВОНЯЩИЙ: ${context.clientName || 'Неизвестно'}${context.clientCompany ? ' из ' + context.clientCompany : ''}`);
    parts.push(`Номер: ${context.callerNumber}`);
    if (context.clientInn) parts.push(`ИНН: ${context.clientInn}`);
  } else {
    parts.push(`ЗВОНЯЩИЙ: Неизвестный номер ${context.callerNumber}`);
    if (context.dadataInfo) parts.push(`Регион: ${context.dadataInfo}`);
  }

  // Ответственный менеджер
  if (context.responsibleManager) {
    parts.push(`Закреплённый менеджер: ${context.responsibleManager} (тел: ${context.managerPhone || 'нет'})`);
  }

  // Последние взаимодействия
  if (context.lastTender) {
    parts.push(`Последний тендер: ${context.lastTender}`);
  }
  if (context.lastCall) {
    parts.push(`Последний звонок: ${context.lastCall}`);
  }

  // История диалога
  if (context.conversationHistory && context.conversationHistory.length > 0) {
    parts.push('');
    parts.push('ДИАЛОГ:');
    for (const turn of context.conversationHistory) {
      const role = turn.role === 'client' ? 'Клиент' : (turn.role === 'system' ? 'СИСТЕМА' : 'Оператор');
      parts.push(`${role}: ${turn.text}`);
    }
  }

  // Последнее сообщение клиента
  if (context.lastClientMessage) {
    parts.push('');
    parts.push(`КЛИЕНТ СКАЗАЛ: "${context.lastClientMessage}"`);
  }

  // Доступные сотрудники для перевода
  if (context.availableEmployees) {
    parts.push('');
    parts.push('ДОСТУПНЫЕ СОТРУДНИКИ:');
    parts.push(context.availableEmployees);
  }

  parts.push('');
  parts.push('Что ответить? (JSON)');

  return parts.join('\n');
};


